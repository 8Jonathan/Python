number = int(input("请输入今年的利润："))
if number <= 100000:
    tema = number * 0.1
    print("应该发放的奖金总数是：",tema,sep='')
elif 100000 < number <= 200000:
    tema = 100000 * 0.1 + (number-100000) * 0.075
    print("应该发放的奖金总数是：",tema,sep='')
elif 200000 < number <= 400000:
    tema = 100000 * 0.1 + 100000 * 0.075+(number-200000) * 0.05
    print("应该发放的奖金总数是：",tema,sep='')
elif 400000 < number <= 600000:
    tema = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (number-4000000) * 0.03
    print("应该发放的奖金总数是：",tema,sep='')
elif 600000 < number <= 1000000:
    tema = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (number-600000) * 0.015
    print("应该发放的奖金总数是：",tema,sep='')
elif 1000000 < number:
    tema = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (number-1000000) * 0.01
    print("应该发放的奖金总数是：",tema,sep='')
